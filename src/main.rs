mod bfs;
mod utils;


fn main() {
    let maze = get_maze();
    let start = (0, 1);
    let end = (9, 18);

    if let Some(path) = bfs::bfs(&maze, start, end) {
        println!("\nBFS path ({} steps):", path.len());
        for step in path {
            println!("{:?}", step);
        }
    }
}





